class ObjectCalculator {
    int x;
    int y;

    public ObjectCalculator(int x, int y){
        this.x = x;
        this.y = y;
    }

    int add(){
    return x+y;
    }

    int sub(){
        return x-y;
    }

    int multi(){
        return x*y;
    }

    int div(){
        return x/y;
    }
}
